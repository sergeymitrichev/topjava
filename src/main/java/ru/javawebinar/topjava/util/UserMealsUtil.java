    public static List<UserMealWithExceed> getFilteredWithExceeded(List<UserMeal> mealList, LocalTime startTime, LocalTime endTime, int caloriesPerDay) {
        // TODO return filtered list with correctly exceeded field
        Stream<UserMeal> stream = mealList.stream();
        Map<LocalDate, Integer> map = stream.collect(Collectors.groupingBy(
                (p) -> p.getDateTime().toLocalDate(),
                Collectors.summingInt(UserMeal::getCalories)
        ));//.forEach();


        return mealList.stream()
                .filter(m -> TimeUtil.isBetween(m.getDateTime().toLocalTime(), startTime, endTime))
                .map(m -> new UserMealWithExceed(m, map.get(m.getDateTime().toLocalDate()) > caloriesPerDay))
                .collect(Collectors.toList());
    }
